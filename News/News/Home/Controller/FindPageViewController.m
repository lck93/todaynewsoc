- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"reuseIdentifier" forIndexPath:indexPath];
    if (!cell) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue2 reuseIdentifier:@"reuseIdentifier"];
    }
    Person *p = self.dataArray[indexPath.row];
    cell.textLabel.text = [NSString stringWithFormat:@"%d",p.pk];
    cell.textLabel.textColor = [UIColor blueColor];
    cell.detailTextLabel.text = p.favorite;
    cell.detailTextLabel.textColor = [UIColor blueColor];
    cell.detailTextLabel.font = [UIFont systemFontOfSize:12.0];
    // Configure the cell...
    
    return cell;
}


